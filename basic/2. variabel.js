// let (Varibel yang bisa diubah nilainya)

let nama = 'Yurdan Syah'
let age = 25

console.log(`My name is ${nama} and I am ${age} years old`)

// Mengubah variabel let

nama = 'alice'

console.log(`My name is ${nama} and I am ${age} years old`)


// const (variabel yang tidak bisa diubah nilainya)
console.log('\n------Const-------')
const pi = 3.14
console.log('Nilai const pi:',pi)

// Coba mengubah nilai const
// pi = 2.3

// var (variabel yang bisa diubah nilainya, namun sudah tidak direkomendasikan untuk digunakan)
console.log('\n------Var (tidak disarankan lagi)-------')
var city = 'Jakarta'
console.log(city)

// Mengubah variabel var
city = 'Bandung'
console.log(city)