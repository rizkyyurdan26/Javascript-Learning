const array1 = ['Budi', 'Adi', 'Riki']

// menambah object: push()
array1.push("Dina")
console.log(array1)

// Menambahkan object di depan unshift()
array1.unshift('Guntur')
console.log(array1)

// menghapus dari depan shift()
array1.shift()
console.log(array1)

// Menghapus dari belakang pop()
array1.pop()
console.log(array1)
